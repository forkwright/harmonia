// Curated headphone EQ profiles from the AutoEQ project (MIT license)
import type { HeadphoneProfile, ParametricBand } from '../types'

function pk(frequency: number, gain: number, q: number): ParametricBand {
  return { type: 'peaking', frequency, gain, q }
}
function ls(frequency: number, gain: number, q: number): ParametricBand {
  return { type: 'low_shelf', frequency, gain, q }
}
function hs(frequency: number, gain: number, q: number): ParametricBand {
  return { type: 'high_shelf', frequency, gain, q }
}

function hp(manufacturer: string, model: string, parametricEq: ParametricBand[]): HeadphoneProfile {
  return { manufacturer, model, parametricEq }
}

